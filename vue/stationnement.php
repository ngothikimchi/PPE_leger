        <div class="sous_title center color_text1">Les personnes handicapées ont leur place </div>
        <div class="text_info center color_text1" >
        Au fur et à mesure des années, la ville aménage des places de stationnement adaptées aux personnes handicapées.<br>
Ces places sont réservées aux personnes détentrices du macaron Grand Invalide de Guerre (G.I.G.) ou Grand Invalide Civil
(G.I.C.). L'occupation de ces places par des véhicules non-autorisés constitue une infraction. Mais respecter la règle, 
ce n'est pas seulement se mettre à l'abri d'une amende, 
c'est aussi et surtout contribuer à l'amélioration des conditions de déplacement des personnes handicapées.  <br>
<span style="color:red;">Ne pas respecter ces places réservées est passible d'une contravention de 135 € et mise en fourrière du véhicule.</span>
