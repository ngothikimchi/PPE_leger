<div class="sous_title center color_text1">Stationnement </div>
     <div class="text_info center color_text1" >Le stationnement est réglementé 
         depuis le 1er décembre 2017, en fonction de la durée autorisée. Trois zones sont 
         définies : la zone jaune pour une durée très courte, la zone rouge pour un temps plus 
         long et enfin la zone verte correspondant à une demi-journée, afin de favoriser le stationnement 
         local et de permettre une rotation des véhicules 
         sur les places de stationnement affectées en particulier en centre-ville et à proximité des commerces.<br>
         Pour chacune des zones, un disque de stationnement, posé sur le tableau de bord, doit indiquer l'heure d'arrivée du véhicule.<br>
         De nombreux parkings offrent la possibilité de stationner la journée (gratuit et réglementé) que ce soit
          à proximité des gares ou des lieux de commerce :<br>
          <ul>
              <li><b>place du Père Jacques-Hamel </b>: 60 places (zone verte et rouge les mercredis et samedis)</li>
              <li><b>rue Jean Mermoz </b>: 63 places dont 2 réservées aux handicapés (zone verte et rouge les mercredis et samedis)</li>
              <li><b>parkings d'Intérêt Régional (P.I.R.) </b>: près des gares d'Ermont-Halte rue de la Halte, 230 places dont 6 réservées aux handicapés (gratuit) et d'Ermont-Eaubonne : 510 places (payant)</li>
              <li><b>parking du marché rue Saint-Flaive Prolongée</b> : 88 places dont 4 réservées aux handicapés (sauf les jours de marché le mercredi matin et le samedi matin) ;</li>
              <li><b>place Jules Ferry </b>: 8 places (stationnement à durée non réglementée)</li>
              <li><b>parking du parc Beaulieu </b>: 68 places dont 4 réservées aux handicapés (zone verte et rouge les mercredis et samedis)</li>
              <li><b>parking rue des Batignons </b>: 23 places (stationnement à durée non réglementée)</li>
              <li><b>parking du Gros-Noyer</b> : 30 places (zone verte)</li>
              <li><b>2 parkings rue Jean-Jaurès </b>: 17 et 24 places (zone verte)</li>
              <li><b>parking rue Edouard-Branly </b>: 25 places (zone verte)</li>
              <li><b>parking de l'Audience </b>: 100 places (stationnement à durée non réglementée)</li>
              
