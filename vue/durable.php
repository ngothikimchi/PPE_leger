     <div class="sous_title center color_text2">Les "toutounets"</div>
     <div class="text_info center color_text1" >
     Afin de limiter les déjections canines sur les trottoirs, la ville vous encourage vivement à adopter un geste simple : 
     ramasser les dejections de vos animaux de compagnie grâce aux 27 "toutounets" présentes sur le territoire communal. 

        Ces distributeurs de sacs à déjections canines sont mis gratuitement à la disposition des 
        propriétaires de chiens et sont rechargés chaque semaine (250 000 sacs commandés à l'année).
