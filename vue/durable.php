    <div class="sous_title center color_text2">Agenda 21 (2018-2020) </div>
    <div class="text_info center color_text1" >
        Le développement durable fait désormais partie intégrante des orientations stratégiques des Etats. Mais c’est bien à 
        l’échelle des territoires que se dessine des solutions, inscrivant concrètement le développement durable dans les réalités
        sociales et économiques. Sur le plan local en effet, des solutions pertinentes existent, simples ou novatrices, qui font 
        d’une ville comme Ermont un lieu de vie plus agréable, un territoire plus résistant, une communauté plus soudée avec des 
        politiques publiques tournées vers demain.<br>
        Après un premier plan d’actions portant sur la période 2012-2017, la Ville d’Ermont lance le deuxième acte de son Agenda 21 pour
        la période 2018-2020.<br>

        Conçu avec des habitants, des entrepreneurs, les services municipaux et les élus, ce nouvel Agenda 21 cherche à offrir une vision 
        positive du territoire et de son avenir autour de quelques sujets cruciaux comme la transition vers une économie circulaire pour
        produire et consommer mieux, la volonté de tisser des liens sociaux durables pour que l’isolement ne soit pas une fatalité, 
        ou encore le renforcement de l’éducation à la nature pour mieux comprendre notre lien vital à elle. Car l’ampleur des défis à 
        relever implique notre responsabilité envers les autres, la planète et les générations futures, la mise en œuvre de 
        l’Agenda 21 d’Ermont s’appuie sur la participation renforcée des citoyens, des acteurs économiques et des services municipaux.<br>
     </div>

