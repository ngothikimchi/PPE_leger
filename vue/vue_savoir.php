<div id="blason" class="sous_title center color_text1">Le blason d'Ermont</div>
<div class="text_info center color_text1" >Le plant de vigne rappelle la culture 
    florissante de la vigne sur les terres d'Ermont. Les fleurs de lys, sur fond bleu azur, symbolisent la terre d'Ile de France et la mitre argentée au centre évoque Saint Flaive, le patron de la ville d'Ermont. La bordure chargée de neuf tourteaux évoque la chaussée Jules César, sous laquelle ont été retrouvées des pièces de monnaie romaines. La couronne murale est l'emblème que portaient les déesses grecques tutélaires des cités et les branches de chênes
     ornées de glands d'argent rappellent la proche forêt de Montmorency.</div> <br><br>
