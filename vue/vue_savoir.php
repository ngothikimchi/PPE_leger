<div class="title_info1 center color_text1">Histoire</div>
<div id="histoire" class="sous_title center color_text1">Un peu d'histoire…</div>
<div class="text_info center color_text1" >
C'est au néolithique que se fixent les premières populations en 
Vallée de Montmorency et particulièrement à Ermont. Progressivement 
des sites de défense et des villages gaulois se développent jusqu'à la conquête romaine qui,
 avec l'extension de son réseau routier, favorise l'essor économique d'Ermont.

A partir du Moyen Age, le village prend de l'importance en se groupant 
autour de l'église. Sa population est principalement paysanne et vit surtout du revenu de ses vignes.

Au 19ème siècle, l'implantation du chemin de fer contribue au développement 
urbain de notre cité.
Au début du 20ème siècle, la croissance démographique consécutive à l'industrialisation 
s'accompagne du développement des constructions pavillonnaires. Ces dernières s'établissent autour du
 nœud ferroviaire des quatre gares situées sur notre territoire.

A la fin de la 2ème Guerre Mondiale, la population est de 9 325 habitants.
 Aujourd'hui, la commune en compte plus de 28 404. Elle se présente avec un patrimoine
  pavillonnaire de 66 %, une zone d'habitats collectifs adaptés à une population diversifiée 
  (37 % de logements sociaux), de nombreux espaces verts et de détente, des voies de circulation
   élargies et des services urbains adaptés. Paroisse devenue commune en 1789, Ermont s'est transformé 
   de village de vignerons et de maraîchers en cité de cheminots puis en commune résidentielle. 
   De nombreux maires ont accompagnés cette évolution. Depuis la Libération, sept maires se sont succédés : 
   Louis Dessard (1945-1947), Louis Savoie (1947-1949), Octave Obré (1949-1959), Robert Bichet (1959-1971), 
   Jacques Berthod (1971-1996), Hugues Portelli (1996-2020) et Xavier Haquin (depuis 2020).
</div>
