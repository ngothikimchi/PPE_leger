     <div class="title_info1 center color_text1">Action Educative</div>
     <div class="text_info center color_text1" >
     La semaine scolaire est organisée sur quatre jours.
Les écoles maternelles et élémentaires ouvrent leurs portes à partir de 8h20 le matin et à 13h20 l'après-midi, et ferment à 11h30 et 16h30, les lundis, mardis, jeudis et vendredis.
Il n'y a pas d'école le mercredi.
La Direction de l’Action éducative est composée de deux services :   
<ul>
    <li>Le service Education et restauration</li>
    <li>Le service des accueils de loisirs ALSH</li>
</ul>  
     </div>
     <div class="sous_title center color_text1">Les missions des services éducation, restauration scolaire et accueils de loisirs </div>
     <div class="text_info center color_text1" >
     <b>MISSIONS OBLIGATOIRES POUR LES ÉCOLES MATERNELLES ET ÉLÉMENTAIRES    </b>
     <ul>
         <li>Inscriptions scolaires primaires et demandes de dérogation à la carte scolaire primaire ;</li>
         <li>Suivi de la scolarité des élèves de 3 à 16 ans ;</li>
         <li>Participation au suivi de l’entretien et des travaux dans les locaux scolaires et périscolaires aux côtés des services techniques ;</li>
         <li>Commande des meubles et du matériel liés à la scolarité en maternelle et en élémentaire (bureaux, jeux, livres, vidéoprojecteurs interactifs (VPI)).</li>

     </ul>

     <b>MISSIONS FACULTATIVES </b>
     <ul>
         <li>Aide financière pour la mise en place de stages et séjours par les écoles élémentaires</li>
         <li>Organisation de la restauration scolaire, de l’étude</li>
         <li>Organisation du temps périscolaires, des accueils de loisirs (pré et postscolaire, étude dirigée, mercredi et vacances), gestion des inscriptions, calcul du quotient familial et facturation</li>
     </ul>

     <b>pour les collèges </b>
     <ul>
         <li>Organisation des échanges scolaires proposés aux collèges avec les pays des villes jumelées</li>
         <li>Financement de projets mis en place pour les collèges</li>
     </ul>
