<div class="title_info1 center color_text1">Écouter, Évaluer et Orienter</div>
<div id="maison" class="sous_title center color_text1">La Maison communale des Solidarités </div>
<div class="text_info center color_text1" >
Un accueil tout public, <b>généraliste</b> et <b>pluridisciplinaire</b>, qui propose des aides administratives, 
services sociaux, aide à la parentalité, permanences d’accès aux droits, lutte contre la fracture numérique 
par la mise à disposition de matériels informatiques et de personnels dédiés.
La Maison communale des Solidarités sera aussi un lieu d’informations notamment sur les activités offertes 
par les services de la ville : Conservatoire, Ferme pédagogique, Prévention santé, ...
La Maison communale des Solidarités a été labellisée <b>"Maison France Services"</b> par l'État.<br>
L’accueil au sein de la Maison communale des Solidarités se fait <b>en deux temps</b> :<br>
<ul>
    <li>Un premier agent du Pôle Solidarité se charge de l’accueil principal afin de vous informer,
 d’évaluer votre demande/besoin, vous orienter et vous apporter une réponse, ou prendre un rendez-vous 
 avec un service municipal ou un partenaire (CAF, SSD, MDPH, CNAV, CPAM).</li>
 <li>Un éventuel second accueil a lieu avec un agent spécialisé dans le domaine en lien avec votre demande 
(logement, aides sociales et financières, médiation familiale...) dans un box individuel, favorisant ainsi la confidentialité de votre situation.
Cet échange permet à la fois de monter un dossier et de mettre en place un véritable suivi, en transversalité avec les différents services.
</li>
</ul></div>

