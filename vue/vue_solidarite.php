<div id="conseil_handicap" class="title_info1 center color_text1">Le conseil Ville-Handicap</div>
<img src="./images/RTEmagicC_Logo_conseil_ville_handicap_02.JPG.jpg">

<div class="text_info center color_text1" >
Ne pas pouvoir demander des renseignements à l'accueil d'une administration, 
ne pas pouvoir se déplacer dans sa commune en toute autonomie ou ne pas pouvoir 
participer aux activités mises en place par les services municipaux en matière
 d'éducation ou de culture : autant de situations, différentes, mais qui constituent chacune une expression du handicap.<br>

Avoir la volonté de rétablir l'équilibre social, de promouvoir l'égalité des citoyens 
entraîne, pour une municipalité, la mise en œuvre de moyens pour faciliter une intégration 
de tous à la vie de la cité, tout handicap confondu (moteur, mental…). C'est l'objectif du 
Conseil Ville-Handicap créé sur l'initiative de la Municipalité d'Ermont en décembre 2007.<br>

La première étape de ce conseil a consisté, dans le cadre de ses deux commissions de travail, 
à élaborer la Charte Ville-Handicap ; un diagnostic d'accessibilité des bâtiments communaux 
a été fait, duquel découlera un programme d'investissement pluriannuel.
Ce Conseil Ville-Handicap regroupe services municipaux, associations représentatives des personnes 
handicapées et Ermontois en situation de handicap afin de réfléchir ensemble à la question du handicap dans notre ville.
L'engagement municipal, formalisé dans la Charte Ville-Handicap signée avec les associations et 
institutions partenaires, sera suivi d'un programme d'actions pluriannuel.<br>
</div>
<div id="maison" class="sous_title center color_text1">Un travail de terrain</div>
<div class="text_info center color_text1" >Parallèlement au travail sur la charte, le Conseil Ville-Handicap continue de recenser les souhaits des personnes 
    en situation de handicap, comme c'est le cas lors du Forum des Associations et de la Vie Locale chaque année.
</div>

