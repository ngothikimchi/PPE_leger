<div id="logement" class="title_info1 center color_text1">Service Logement</div>
<div class="text_info center color_text1" >
<b>Horaires d'ouverture de la permanence</b><br>
Ouvert du lundi au jeudi : 8h45-12h / 13h30-17h45<br>
Vendredi : 8h45-12h / 13h30-16h45<br>
<b>Accueil téléphonique : 01 30 72 37 94</b><br>
Le Service municipal Logement a été créé début 2002 par la municipalité.
Il est enregistreur de la demande de logement social. Il vous permet de remplir un dossier afin d'obtenir <b>un numéro unique régional</b>. Ce numéro est obligatoire pour toute demande initiale de logements sociaux en région Ile-de-france, pour les départements 75, 77, 78, 91, 92, 93, 94, 95.

Cette demande peut être indifféremment enregistrée la 1ère fois soit :
    <ul>
        <li>en mairie</li>
        <li>chez les bailleurs sociaux</li>
    </ul>
    Le renouvellement annuel se fera sur le lieu de domiciliation

Le service logement reçoit les demandes pendant ses heures d'ouverture au public (trois demi-journées par semaine) ou par courrier ou par e-mail.

En cas de vacance de logement communal ou préfectoral, le service logement propose trois candidats, (en fonction de la composition familiale, des revenus et de l'ancienneté de la demande), approuvés en premier 
lieu par la commission Mairie. Puis après approbation et ordre de passage choisi, les dossiers sont envoyés au bailleur concerné.
</div>

