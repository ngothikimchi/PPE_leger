<div id="eau" class="title_info1 center color_text1">Aide personnalisée pour les dépenses d’eau</div>
<img src="./images/Bandeau_cheque_eau.jpg">
<div class="text_info center color_text1" >
Mis en œuvre dans le cadre du programme Eau solidaire du SEDIF et mis en place par Veolia Eau Île-de-France, 
l'aide Chèque eau permet le paiement de vos factures d'eau en cas de difficultés ponctuelles.<br>

Pour en bénéficier, l'abonné doit se rendre au CCAS (pour les personnes isolées) ou au Service social départemental 
(pour les couples avec ou sans enfants et familles monoparentales). Le travailleur social instruit la demande pour 
étude à la commission du CCAS. Celle-ci verse l'aide accordée directement au prestataire. L'abonné règle le montant restant 
de la facture à Veolia Eau d'Île-de-France.
 L'équipe Eau solidaire de Veolia Eau d'Île-de-France enregistre l'aide et le versement complémentaire : <b>la facture de l'abonné est payée</b>.
</div>
