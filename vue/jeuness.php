<div class="title_info1 center color_text1">Secteur jeunesse</div>
<div class="text_info center color_text1" >
Dans une volonté de répondre aux besoins de tous les Ermontois de 12 à 25 ans, sans exception, 
la municipalité étend le secteur jeunesse sur l'ensemble du territoire. <br>
<b>Plus de structure unique, la jeunesse rayonne sur toute la ville ! </b><br>

Depuis le 15 mai 2021, les jeunes Ermontois de 12 à 25 ans retrouvent près de chez eux, les nombreuses 
activités organisées par la ville en leur faveur.<br>

L'espace jeunesse du 37 bis rue Maurice-Berteaux laisse place au "secteur jeunesse" déployé sur toute la ville.<br>

<b>Le secteur jeunesse </b>est regroupé dans les Centres socio-culturels les Chênes, Francois-Rude et la maison de quartier 
des Espérences-Arts, qui accueillent ainsi en leur sein, les équipes d'animation spécialisées dans le jeune public.<br>

<b>Les adresses changent, les services restent ! </b><br>
La totalité des services, d'animation et d'information, est maintenue.<br>
