    Afin d’assurer une protection maximale sur le territoire, en complémentarité avec la Police Nationale, 
    la Police Municipale a mis en place une brigade de soirée. Tous les soirs, les agents effectuent des patrouilles 
    jusqu’à 22h (sauf dimanche et hors périodes de vacances scolaires).<br>

    Les policiers municipaux sont placés sous la tutelle directe et exclusive de Monsieur le Maire, avec les priorités suivantes :
    <ul>
        <li>surveiller la voie publique, les lieux ouverts au public et les parties communes des bailleurs d’immeuble.</li>
        <li>développer une relation d’aide, d’assistance et de proximité avec les Ermontois.</li>
        <li>sécuriser les établissements scolaires et leurs abords.</li>
    </ul>
    Le service se compose de 17 agents (actuellement en cours de recrutement) :
    <ul>
        <li>11 policiers municipaux dont 1 agent cynotechnicien (conducteur de chien)</li>
        <li>5 agents de surveillance de la voie publique</li>
        <li>1 agent d’accueil</li>
    </ul>
    Les effectifs de la police municipale sont renforcés par les équipes de la brigade anti-criminalité et 
    les brigades de voies publiques du commissariat divisionnaire d’Ermont de jour comme de nuit.<br>

Enfin, l’équipe de police d’Ermont travaille de concert avec le Centre de supervision urbain intercommunale 
situé sur la commune de Saint-Prix, responsable de la vidéoprotection sur le territoire de Val Parisis. <br>
