        <span style="font-weight:bolder;font-size:20px;">SYNOPSIS</span><br>
        Tout sourit à Audrey et Jérôme. Ils ont trois merveilleux enfants et leurs
         métiers les passionnent. Le temps d’un week-end, ils partent chacun de leur côté... Avec leurs 
         amants respectifs. Sauf qu’ils ont la même idée : aller dans leur maison de campagne. Quand ils se retrouvent 
         nez à nez, c'est l'explosion. Arrivent alors les parents d’Audrey, puis leurs enfants et enfin sa sœur. 
         Le quatuor n’a pas d’autre choix que jouer la comédie
         pour sauver les apparences. Mais très vite le vernis et les nerfs craquent...
